using System;

namespace Curso_csharp_3_variables
{
    class Program
    {
        static void Main(string[] args)
        {
            char caracter = 'k';
            Console.WriteLine(caracter);

            String cadena_de_caracteres = "khristopher Pineda";
            Console.WriteLine(cadena_de_caracteres);

            int entero = 3;
            Console.WriteLine(entero);

            long entero2 = 2147483648;
            Console.WriteLine(entero2);

            float float1 = 2.147483648f;
            Console.WriteLine(float1);

            double float2 = 2.147483648;
            Console.WriteLine(float2);

            decimal decimal1 = 2.1474836486238746m;
            Console.WriteLine(decimal1);

            Console.ReadLine();
        }
    }
}
